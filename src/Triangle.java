public class Triangle {
    // To make perfect triangle, you have to use odd numbers of stars in each lines
    public static void main( String [] args) {
        System.out.println("      *     ");
        System.out.println("     ***    ");
        System.out.println("    *****   ");
        System.out.println("   *******  ");
        System.out.println("  ********* ");
        System.out.println(" ***********");
    }
}
